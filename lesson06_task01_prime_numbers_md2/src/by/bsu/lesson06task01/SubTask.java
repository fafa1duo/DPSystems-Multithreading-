package by.bsu.lesson06task01;

public class SubTask {
	// подзадача - поиск простых в диапазоне
	private int min;
	private int max;

	public SubTask(int min, int max) {
		super();
		this.min = min;
		this.max = max;
	}

	public int getMin() {
		return min;
	}

	public void setMin(int min) {
		this.min = min;
	}

	public int getMax() {
		return max;
	}

	public void setMax(int max) {
		this.max = max;
	}

}
